class User {
    constructor(id, nombre, username, password, email, pais, provincia, genero, nacimiento, telefono) {
      this.id = id;
      this.nombre = nombre;
      this.username = username;
      this.password = password;
      this.email = email;
      this.pais = pais;
      this.provincia = provincia;
      this.genero = genero;
      this.nacimiento = nacimiento;
      this.telefono = telefono;
    }
  }